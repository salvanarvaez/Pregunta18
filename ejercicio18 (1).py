from statsmodels.tsa.arima.model import ARIMA

def fit_model(order, name):
    try:
        model = ARIMA(df["log_PDI"], order=order).fit()
        print(f"{name} {order} - AIC: {model.aic:.2f}")
        return model
    except:
        print(f"{name} {order} - Fall贸")
        return None

# Modelos candidatos
